public class City {
    private double center_x;
    private double center_y;

    public City(double center_x, double center_y) {
        this.center_x = center_x;
        this.center_y = center_y;
    }

    public double getCenter_x() {
        return center_x;
    }

    public double getCenter_y() {
        return center_y;
    }
}
